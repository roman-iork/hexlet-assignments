package exercise;

public class IntegerStringEntry {
    private int value1;
    private String value2;

    public IntegerStringEntry(int value1, String value2) {
        this.value1 = value1;
        this.value2 = value2;
    }

    public int getValue1() {
        return value1;
    }

    public String getValue2() {
        return value2;
    }
}
